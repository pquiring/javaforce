  public void doGet(WebRequest req, WebResponse res) {
    String url = req.getURL();
    if (url.length() == 0) url = "/";
    if (url.equals("/")) {
      String html;
      if (doImage) {
        html = getHTMLImage();
      } else {
        html = getHTMLVideo();
      }
      try {
        res.write(html.getBytes());
      } catch (Exception e) {}
    }
    if (url.endsWith(selected_codec.ext)) {
      //send segment
      String file = JF.getTempPath() + url;
      if (!tempFiles.contains(file)) {
        JFLog.log("WebMediaNotFound:" + file);
        res.setStatus(404, "File not found");
        return;
      }
      try {
        res.setContentType(selected_codec.mime);
        FileInputStream fis = new FileInputStream(file);
        byte[] data = fis.readAllBytes();
        fis.close();
        res.write(data);
      } catch (Exception e) {}
    }
    if (url.endsWith(".jpg")) {
      if (image == null) {
        res.setStatus(404, "Not Ready");
        return;
      }
      try {
        res.write(image);
      } catch (Exception e) {}
    }
    if (url.endsWith(".mpd")) {
      if (mpd == null) {
        res.setStatus(404, "Not Ready");
        return;
      }
      try {
        res.write(mpd);
      } catch (Exception e) {}
    }
    if (url.endsWith(".idx")) {
      int idx = segment;
      if (tempFiles.size() > 1) {
        idx -= tempFiles.size() - 1;
      }
      try {
        res.write(Integer.toString(idx).getBytes());
      } catch (Exception e) {}
    }
  }

