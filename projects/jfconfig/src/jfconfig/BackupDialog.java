  public boolean accepted, nodest;

  private void listDest() {
    dest.removeAllItems();
    File file = new File("/media");
    File files[] = file.listFiles();
    if (files.length == 0) {
      nodest = true;
      return;
    }
    for(int a=0;a<files.length;a++) {
      dest.addItem(files[a].getName());
    }
  }

  public String getDest() {
    return (String)dest.getSelectedItem();
  }

  public int getCompression() {
    return compression.getSelectedIndex();
  }

  public int getDeleteDays() {
    if (deleteOld.isSelected()) return (Integer)deleteDays.getValue();
    return -1;
  }

  private void setPosition() {
    Rectangle s = GraphicsEnvironment.getLocalGraphicsEnvironment().getMaximumWindowBounds();
    Dimension d = getSize();
    setLocation(s.width/2 - d.width/2, s.height/2 - (d.height/2));
  }
