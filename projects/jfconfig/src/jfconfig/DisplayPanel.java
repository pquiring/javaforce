  public void mouseDragged(MouseEvent me) {
    if (!drag) return;
    JMonitor jmonitor = (JMonitor)me.getSource();
    int x = me.getXOnScreen();
    int y = me.getYOnScreen();
//    System.out.println("dragged:" + x + "," + y);
    jmonitor.setLocation(ix + (x - sx), iy + (y - sy));
  }

