  public void local_mkdir() {
    String fn = JF.getString("Enter new folder name", "");
    if ((fn == null) || (fn.length() == 0)) {
      return;
    }
    if (fn.indexOf(":") != -1) {
      return;
    }
    if (fn.indexOf("/") != -1) {
      return;
    }
    if (fn.indexOf("?") != -1) {
      return;
    }
    if (fn.indexOf("*") != -1) {
      return;
    }
    String wd = localDir.getText();
    if (!wd.endsWith("/")) {
      wd += "/";
    }
    try {
      local_mkdir(wd + fn);
      localFolders.addSetTag(localTag, fn, "", "");
      local_chdir(".");  //refresh
    } catch (Exception e) {
      JF.showError("Error", "Create folder failed:" + e);
    }
  }

