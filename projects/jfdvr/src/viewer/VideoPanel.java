  public void setImage(JFImage src) {
    init();
    if (video == null) return;
    if (src.getWidth() == getWidth() && src.getHeight() == getHeight()) {
      video.putJFImage(src, 0, 0);
    } else {
      video.putJFImageScale(src, 0, 0, getWidth(), getHeight());
    }
    update();
  }

  public void setImage(JFImage src, int px, int py) {
    init();
    if (video == null) return;
    if (zoom) {
      if (px != zx || py != zy) return;
      setImage(src);
      return;
    }
    int img_w = getWidth();
    int img_h = getHeight();
    int w = img_w / gx;
    int h = img_h / gy;
    int x = w * px;
    int y = h * py;
    setImageRect(src, x, y, w, h);
    update();
  }

  private void setImageRect(JFImage src, int x, int y, int w, int h) {
    init();
    if (video == null) return;
    video.putJFImageScale(src, x, y, w, h);
  }

  private void update() {
    if (needPainting) {
      if (debug) JFLog.log("Video updating too slow");
      return;
    }
    needPainting = true;
    long now = System.currentTimeMillis() / 1000L;
    if (lastUpdate != now) {
      updateTimelines();
      minutes.setIcon(timeline_min);
      seconds.setIcon(timeline_sec);
      lastUpdate = now;
    }
    try {
      java.awt.EventQueue.invokeLater(new Runnable() {
        public void run() {
          repaint();
        }
      });
    } catch (Exception e) {}
  }

  public void paintComponent(Graphics g) {
    int w = getWidth();
    int h = getHeight();
    //paint controls
    if (video == null) {
      g.fillRect(0, 0, w, h);
    } else {
      int iw = video.getWidth();
      int ih = video.getHeight();
      if (((iw != w) || (ih != h))) {
        if (debug) JFLog.log("VideoPanel:image scaled");
        JFImage scaled = new JFImage();
        scaled.setImageSize(w, h);
        scaled.getGraphics().drawImage(video.getImage(), 0,0, w,h, 0,0, iw,ih, null);
        video = scaled;
      }
      g.drawImage(video.getImage(), 0, 0, null);
    }
    needPainting = false;
  }

  public void setGrid(int gx, int gy) {
    if (debug) JFLog.log("setGrid:" + gx + "," + gy);
    cancelTimer();
    container.setVisible(false);
    grid = true;
    this.gx = gx;
    this.gy = gy;
  }

  private void zoom(int x, int y) {
    if (!grid) return;
    if (zoom) {
      zoom = false;
    } else {
      int img_w = getWidth();
      int img_h = getHeight();
      int w = img_w / gx;
      int h = img_h / gy;
      zx = x / w;
      zy = y / h;
      zoom = true;
    }
  }

  private static final int BLACK = 0x000000;
  private static final int GREEN = 0x00ff00;
  private static final int BLUE = 0x0000ff;

  private void updateTimelines() {
    boolean past_day = get_date_delta() > 0;
    boolean past_min = ts_delta > ms_per_min;
    long now = JF.currentTimeMillis();
    long day = toDay(now);
    long min = toMin(now);
    long sec = toSec(now);
    if (debug) JFLog.log("time:" + now + ":" + (day/ms_per_day) + "," + (min/ms_per_min) + "," + (sec/ms_per_sec) + ":" + past_day);
    for(int x=0;x<min_per_day;x++) {
      long cmin = x * ms_per_min;
      int clr = BLACK;
      if (past_day) {
        clr = GREEN;
      } else {
        if (cmin <= min) {
          clr = GREEN;
        }
      }
      if (cmin == min) {
        clr = BLUE;
      }
      timeline_min.line(x, 0, x, 15, clr);
    }
    for(int x=0;x<60;x++) {
      long csec = x * 1000;
      int clr = 0;
      if (past_min) {
        clr = GREEN;
      } else {
        if (csec <= sec) {
          clr = GREEN;
        }
      }
      if (csec == min) {
        clr = BLUE;
      }
      timeline_sec.line(x, 0, x, 15, clr);
    }
  }

