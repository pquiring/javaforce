  private synchronized void init() {
    if (video != null) return;
    int x = getWidth();
    int y = getHeight();
    if (x > 0 && y > 0) {
      video = new JFImage(x, y);
    }
  }

  private void wake() {
    cnt = 0;
    if (grid || container.isVisible()) return;
    java.awt.EventQueue.invokeLater(new Runnable() {
      public void run() {
        if (debug) JFLog.log("show controls");
        container.setVisible(true);
        revalidate();
        repaint();
      }
    });
  }

  private void createTimer() {
    timer = new java.util.Timer();
    timer.schedule(new TimerTask() {
      public void run() {
        if (!container.isVisible()) return;
        cnt++;
        if (cnt == 5) {
          java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
              if (debug) JFLog.log("hide controls");
              container.setVisible(false);
              repaint();
            }
          });
        }
      }
    }, 1000, 1000);
  }

  private void cancelTimer() {
    if (timer != null) {
      timer.cancel();
      timer = null;
    }
  }

  /** Removes timestamps from URL */
  public static String cleanURL(String url) {
    //rtsp://user:pass@server/camera/name/[ts_start/[ts_end]]
    int i1 = url.indexOf('/', 7);
    int i2 = url.indexOf('/', i1+1);
    if (i2 == -1) return url;
    int i3 = url.indexOf('/', i2+1);
    if (i3 == -1) return url;
    return url.substring(0, i3);  //strip timestamps
  }

  public void setURL(String url, String path) {
    this.url = cleanURL(url);
    String[] p = path.substring(1).split("/");
    //camera / name / ts_start / ts_end
    String ts_start = null;
    String ts_end = null;
    switch (p.length) {
      case 2: break;
      case 3: ts_start = p[2]; break;
      case 4: ts_start = p[2]; ts_end = p[3]; break;
    }
    if (ts_start != null) {
      SpinnerDateModel model = (SpinnerDateModel)date.getModel();
      model.setCalendarField(Calendar.DAY_OF_MONTH);
      model.setValue(new Date(Long.valueOf(ts_start)));
    }
  }

