  private void timeline_mins_mouse_move(int x, int y) {
    if (x >= 1440) x = 1439;
    int mins = x;  //1440
    int hour = x / 60;  //24
    mins -= hour * 60;  //60
    String am_pm = null;
    String text = null;
    if (hour >= 12) {
      am_pm = "PM";
      hour -= 12;
    } else {
      am_pm = "AM";
    }
    if (hour == 0) hour = 12;
    text = String.format("%02d:%02d%s", hour, mins, am_pm);
    minutes.setToolTipText(text);
  }

  private void timeline_secs_mouse_move(int x, int y) {
    if (x >= 60) x = 59;
    int secs = x;  //60
    String text = String.format("%02d", secs);
    seconds.setToolTipText(text);
  }

  private static final long ms_per_day = 24 * 60 * 60 * 1000;  //hr * min * sec * 1000
  private static final long ms_per_hour = 60 * 60 * 1000;  //min * sec * 1000
  private static final long ms_per_min = 60 * 1000;  //sec * 1000
  private static final long ms_per_sec = 1000;
  private static final int min_per_day = 24 * 60;  //hr * min

  private long toDay(long ts) {
    //get days since epoch * ms_per_day (represents time at midnight)
    ts /= ms_per_day;
    ts *= ms_per_day;
    return ts;
  }

  private long toMin(long ts) {
    //get minute within day : 1440 * 1000
    ts %= ms_per_day;
    long secs = ts % ms_per_sec;
    ts -= secs;
    return ts;
  }

  private long toSec(long ts) {
    //get second within minute
    ts = ts % ms_per_min;
    ts /= 1000;
    ts *= 1000;
    return ts;
  }

  private long get_date_delta() {
    //this is all in UTC (no timezones)
    long now = System.currentTimeMillis();
    long today = toDay(now);
    Date dt = (Date)date.getValue();
    long date_day = toDay(dt.getTime());
    if (debug) JFLog.log("today=" + (today / ms_per_min) + ",date=" + (date_day / ms_per_min));
    if (date_day > today) return 0;
    long delta = today - date_day;
    if (debug) JFLog.log("date_delta_mins=" + (delta / ms_per_min));
    return delta;
  }

  private void seek_live() {
    ts_delta = 0;
    try {
      viewer.play(new URI(url).toURL());
    } catch (Exception e) {
      JFLog.log(e);
    }
  }

  private void seek_mins(int mins) {
    if (mins >= 1440) mins = 1439;
    clearSelection();
    long tz_now = JF.currentTimeMillis();
    int now_mins = (int)(toMin(tz_now) / ms_per_min);
    mins = now_mins - mins;
    ts_delta = get_date_delta() + (mins * ms_per_min);
    if (debug) JFLog.log("ts_delta_mins=" + (ts_delta / ms_per_min));
    if (ts_delta < 0) return;
    long now = System.currentTimeMillis();
    long ts = now - ts_delta;
    try {
      viewer.play(new URI(url + "/" + ts).toURL());
    } catch (Exception e) {
      JFLog.log(e);
    }
  }

  private void seek_secs(int secs) {
    if (secs >= 60) secs = 59;
    clearSelection();
    long mins = ts_delta / ms_per_min;
    ts_delta = get_date_delta() + (mins * ms_per_min) + (secs * 1000);
    if (debug) JFLog.log("ts_delta_mins=" + (ts_delta / ms_per_min));
    if (ts_delta < 0) return;
    long now = System.currentTimeMillis();
    long ts = now - ts_delta;
    try {
      viewer.play(new URI(url + "/" + ts).toURL());
    } catch (Exception e) {
      JFLog.log(e);
    }
  }

  private void clearSelection() {
    sel_mins_start = -1;
    sel_secs_start = -1;
    ts_sel_min_start = -1;
    ts_sel_min_stop = -1;
    ts_sel_sec_start = -1;
    ts_sel_sec_stop = -1;
    download.setEnabled(false);
  }

  private void sel_mins_start(int min) {
    sel_mins_start = min;
  }

  private void sel_mins_stop(int min) {
    if (sel_mins_start == min) {
      clearSelection();
      return;
    }
    if (min < sel_mins_start) {
      //swap start/stop
      int tmp = sel_mins_start;
      sel_mins_start = min;
      min = tmp;
    }
    long now = System.currentTimeMillis();
    ts_sel_min_start = now - (get_date_delta() + (sel_mins_start * ms_per_min));
    ts_sel_min_stop = ts_sel_min_start + (min * ms_per_min);
    ts_sel_sec_start = -1;
    ts_sel_sec_stop = -1;
    download.setEnabled(true);
  }

  private void sel_secs_start(int sec) {
    sel_secs_start = sec;
  }

  private void sel_secs_stop(int sec) {
    if (sel_secs_start == sec) {
      clearSelection();
      return;
    }
    if (sec < sel_secs_start) {
      //swap start/stop
      int tmp = sel_secs_start;
      sel_secs_start = sec;
      sec = tmp;
    }
    long mins = toMin(ts_delta);
    long now = System.currentTimeMillis();
    ts_sel_sec_start = now - (sel_secs_start * 1000);
    ts_sel_sec_stop = ts_sel_sec_start + (sec * 1000);
    ts_sel_min_start = -1;
    ts_sel_min_stop = -1;
    download.setEnabled(true);
  }

  private void download() {
    String filename = JFAWT.getSaveAsFile(System.getProperty("user.dir"), new String[][] {
      {"MKV", "mkv"},
      {"MP4", "mp4"},
      {"AVI", "avi"},
    });
    if (filename == null) return;
    viewer.startDownload(filename);
    if (ts_sel_min_start != -1) {
      //download mins
      try {
        viewer.play(new URI(url + "/" + ts_sel_min_start + "/" + ts_sel_min_stop).toURL());
      } catch (Exception e) {
        JFLog.log(e);
      }
    }
    if (ts_sel_sec_start != -1) {
      //download secs
      try {
        viewer.play(new URI(url + "/" + ts_sel_sec_start + "/" + ts_sel_sec_stop).toURL());
      } catch (Exception e) {
        JFLog.log(e);
      }
    }
  }

  public void setTimestamp(long ts) {
    ts_delta = ts;
    if (ts_delta == 0) {
      seek_live();
    }
  }
