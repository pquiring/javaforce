  public FormulaDialog(java.awt.Frame parent, boolean modal, Backend backend) {
    super(parent, modal);
    initComponents();
    elements.setModel(model);
    model.clear();
    for(int a=0;a<backend.formula.size();a++) {
      String str = backend.formula.get(a);
      if (str.length() == 0) continue;
      if (str.charAt(0) == '#') str = str.substring(1);
      int idx = str.indexOf(',');
      if (idx != -1) {
//        str = str.substring(0, idx);  //remove radix
      }
      model.addElement(str);
    }
    this.backend = backend;
    setPosition();
  }

